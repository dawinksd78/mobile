					② 회사는 관련법을 위배하지 않는 범위에서 본 약관을 개정할 수 있습니다.<br>
					③ 회사가 약관을 개정할 경우에는 적용일자 및 개정사유를 명시하여 이용자가 알기 쉽도록 표시하여 공지합니다.<br>
					④ 회사가 약관을 개정할 경우에는 적용일자 및 개정사유를 명시하여 현행약관과 함께 제①항의 방식에 따라 그 개정약관의 적용일자 7일 전부터 적용일자 전일까지 공지합니다. 다만, 회원에게 불리한 약관의 개정인 경우 적용일자 30일 전부터 적용일자 전일까지 서비스 홈페이지에 공지하고 기존 회원에게는 이메일 주소로 개정약관을 발송하여 고지합니다.<br>
