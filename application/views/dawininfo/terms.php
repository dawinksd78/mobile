					① 이 약관은 이용자의 동의와 회사의 승낙으로 효력이 발생합니다. 이용자의 동의는 회원인 경우 회원가입 시 동의절차를 밟으며, 비회원인 경우 웹사이트에 접속하여 서비스를 이용하는 동안 이 약관에 동의한 것으로 간주합니다.<br>
					② 회사는 필요한 경우 전체 서비스 중 일부 콘텐츠, 특정 서비스의 이용에 대하여 개별약관 또는 운영원칙, 이용안내(이하 개별약관)를 설정할 수 있으며, 이 약관과 개별약관의 내용이 상충되는 경우에는 해당 콘텐츠, 특정 서비스에 적용되는 개별약관이 우선 적용됩니다.<br>
					③ 개별약관에서 정하지 아니한 사항에 대하여는 이 약관의 각 조건이 적용될 수 있는 사항인 경우에 이 약관이 공통적 또는 보충적으로 적용됩니다.<br>
					④ 본 약관에서 정하지 아니한 사항이나 해석에 대해서는 개별정책, 관계법령 또는 상관례에 따릅니다.</p><br>
