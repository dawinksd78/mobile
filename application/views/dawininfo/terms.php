					① 회원가입은 이용자가 약관의 내용에 대하여 동의를 한 다음 회원가입 신청을 하고 휴대폰 인증을 한 후 회사가 이러한 신청에 대하여 승낙함으로써 체결됩니다. 회사는 회원가입 승낙의 의사표시를 해당 서비스 화면에 게시하거나 이메일 또는 기타 방법으로 통지합니다.<br>
					② 회사는 다음 각 호에 해당하는 신청에 대하여는 승낙을 하지 않을 수 있습니다.<br>
					<p class="phar">1. 가입신청자가 법령 또는 약관을 위반하여 이전에 회원자격을 상실한 적이 있는 경우, 단 회사의 재가입 승낙을 얻은 경우에는 예외로 함<br>
					2. 회사로부터 회원자격 정지 조치 등을 받은 회원이 그 조치기간 중에 이용계약을 임의 해지하고 재이용 신청을 하는 경우<br>
					3. 타인의 명의를 이용한 경우<br>
					4. 허위의 정보를 입력하거나, 회사가 제시하는 내용을 기재하지 않은 경우<br>
					5. 이용자의 귀책사유로 인하여 승인이 불가능하거나 기타 규정한 제반 사항을 위반하며 신청하는 경우<br>
					6. 부정한 목적 또는 용도로 이 서비스를 이용하고자 하는 경우</p>
					③ 회원 가입 계약의 성립시기는 회사의 승낙이 회원에게 도달한 시점으로 합니다.<br>
					④ 회사는 회원가입 절차의 완료 이후에도 제2항 각 호에 따른 사유가 발견된 경우 회원가입 승낙을 철회할 수 있습니다.<br>
					⑤ 회사는 회원에 대한 회사정책에 따라 등급별로 구분하여 이용시간, 이용횟수, 서비스 메뉴 등을 세분하여 이용에 차등을 둘 수 있습니다.</p><br>
				<p><strong>제6조 (회원정보의 변경)</strong><br>
					① 회원은 회사가 제공하는 화면을 통하여 언제든지 자신의 개인정보를 열람하고 수정할 수 있습니다.<br>
					② 회원은 회원가입 또는 서비스 신청 시 입력한 사항이 변경되었을 경우 온라인으로 수정을 하거나 기타 방법으로 회사에 대하여 그 변경사항을 알려야 합니다.<br>
					③ 제②항의 변경사항을 회사에 알리지 않아 발생한 불이익에 대하여 회사는 책임지지 않습니다.</p><br>
